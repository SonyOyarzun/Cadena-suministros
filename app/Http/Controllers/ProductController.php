            return ['message'=>'Debe ingresar nombre','type'=>'error'];
          } else {
      
            $product = new Product;
            $product->JSON  = $request->JSON;
            $product->created_at = now();
            $product->updated_at = now();
            $product->save();
      
            return ['message'=>'Producto ingresado','type'=>'success'];
